#!/usr/bin/env node

import { mkdir, readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import process from 'node:process';

const MODEL_ID = 'eleven_v3';
const PHRASES_PATH = path.resolve('src/data/hebrewScoreStaticPhrases.json');
const AUDIO_DIR = path.resolve('assets/audio/static-hebrew-scores');
const MANIFEST_PATH = path.resolve('src/services/generated/hebrewScoreStaticAudioManifest.ts');

function getCredentials() {
  const apiKey = (process.env.EXPO_PUBLIC_ELEVENLABS_API_KEY || '').trim();
  const voiceId = (process.env.EXPO_PUBLIC_ELEVENLABS_VOICE_ID || '').trim();
  if (!apiKey || !voiceId) {
    throw new Error(
      'Missing ElevenLabs credentials. Set EXPO_PUBLIC_ELEVENLABS_API_KEY and EXPO_PUBLIC_ELEVENLABS_VOICE_ID.'
    );
  }
  return { apiKey, voiceId };
}

function uniqueByText(entries) {
  const seen = new Set();
  return entries.filter((entry) => {
    const key = String(entry.text || '').trim();
    if (!key || seen.has(key)) return false;
    seen.add(key);
    return true;
  });
}

function toAudioFilename(slug) {
  return `he_score_${slug}.mp3`;
}

function escapeTsString(value) {
  return value.replace(/\\/g, '\\\\').replace(/"/g, '\\"');
}

async function synthesizePhrase({ text, slug }, apiKey, voiceId) {
  const url = `https://api.elevenlabs.io/v1/text-to-speech/${encodeURIComponent(voiceId)}`;
  const response = await fetch(url, {
    method: 'POST',
    headers: {
      Accept: 'audio/mpeg',
      'Content-Type': 'application/json',
      'xi-api-key': apiKey,
    },
    body: JSON.stringify({
      text,
      model_id: MODEL_ID,
    }),
  });

  if (!response.ok) {
    const body = await response.text().catch(() => '');
    throw new Error(`ElevenLabs ${response.status} for "${text}": ${body.slice(0, 200)}`);
  }

  const audioBytes = Buffer.from(await response.arrayBuffer());
  const outPath = path.join(AUDIO_DIR, toAudioFilename(slug));
  await writeFile(outPath, audioBytes);
  return outPath;
}

function buildManifestSource(entries) {
  const lines = [
    '/**',
    ' * Auto-generated by scripts/generate-static-hebrew-score-audio.mjs.',
    ' * Do not edit manually.',
    ' */',
    'export const HEBREW_SCORE_STATIC_AUDIO: Record<string, number> = {',
  ];

  for (const entry of entries) {
    const text = escapeTsString(entry.text.trim());
    const filename = toAudioFilename(entry.slug);
    lines.push(
      `  "${text}": require("../../../assets/audio/static-hebrew-scores/${filename}"),`
    );
  }

  lines.push('};', '');
  return lines.join('\n');
}

async function main() {
  const { apiKey, voiceId } = getCredentials();
  const phrasesRaw = await readFile(PHRASES_PATH, 'utf8');
  const phrases = uniqueByText(JSON.parse(phrasesRaw));

  if (phrases.length === 0) {
    throw new Error('No phrases found in src/data/hebrewScoreStaticPhrases.json');
  }

  await mkdir(AUDIO_DIR, { recursive: true });

  console.log(`Generating ${phrases.length} static Hebrew score files...`);
  for (const phrase of phrases) {
    const text = String(phrase.text || '').trim();
    const slug = String(phrase.slug || '').trim();
    if (!text || !slug) {
      throw new Error(`Invalid phrase entry: ${JSON.stringify(phrase)}`);
    }
    await synthesizePhrase({ text, slug }, apiKey, voiceId);
    console.log(`  ✓ ${slug}`);
  }

  const manifestSource = buildManifestSource(phrases);
  await writeFile(MANIFEST_PATH, manifestSource, 'utf8');
  console.log(`Updated manifest: ${path.relative(process.cwd(), MANIFEST_PATH)}`);
  console.log('Done.');
}

main().catch((error) => {
  console.error(error.message || error);
  process.exit(1);
});
